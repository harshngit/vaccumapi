-- ─── ENUM: job_status ────────────────────────────────────────

DO $$ BEGIN
  CREATE TYPE job_status AS ENUM (
    'Raised',
    'Assigned',
    'In Progress',
    'Closed'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;


-- ─── ENUM: job_priority ──────────────────────────────────────

DO $$ BEGIN
  CREATE TYPE job_priority AS ENUM (
    'Low',
    'Medium',
    'High',
    'Critical'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;


-- ─── ENUM: job_category ──────────────────────────────────────

DO $$ BEGIN
